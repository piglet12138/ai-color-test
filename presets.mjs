export function seasonsRef() {
  return SEASONS.map((s) => `${s.zh}(${s.en})[${s.tone}/${s.value}/${s.chroma}]：${s.palette.map((c) => c[0] + c[1]).join('、')}`).join('\n');
}
export function catalogRef(type, gender) {
  let key = type;
  if (type === 'hairstyle' || type === 'makeup' || type === 'jewelry') key = type + '_' + (gender === '男' ? 'male' : 'female');
  const list = CATALOGS[key];
  if (!list) return '';
  return list.map((x) => (Array.isArray(x) ? x[0] : x)).join('、');
}
export function outfitRef(occasion) {
  return (OUTFIT_FORMULAS[occasion] || []).join(' / ');
}
