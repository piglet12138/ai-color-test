// key -> 风格对象
export function vibeById(key) {
  return MAKEUP_VIBES.find((v) => v.key === key) || null;
}

// 按性别筛选：'男' / '女'；'通用' 的始终返回。传空/其它返回全部。
export function vibesFor(gender) {
  if (gender !== '男' && gender !== '女') return MAKEUP_VIBES.slice();
  return MAKEUP_VIBES.filter((v) => v.gender === gender || v.gender === '通用');
}

// 便捷：拼出某条风格的完整生图 prompt
export function buildPrompt(key, gender) {
  const v = vibeById(key);
  if (!v) return '';
  return IMAGE_TEMPLATE.replace('{desc}', v.desc_for_image).replace(/\{gender\}/g, gender || '这个人');
}
